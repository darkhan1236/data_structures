def power(x, n):
    if not isinstance(x, int) or not isinstance(n, int):
        return "Қате: сандық аргумент енгізіңіз."
    if x < 0 or x > 999:
        return "Қате: x [0..999] диапазонында болуы керек."
    if n < 1 or n > 100:
        return "Қате: n [1..100] диапазонында болуы керек."
    return x ** n

# Тестілеу
test_cases = [
    (2, 3),  # Күтілетін нәтиже: 8
    (-1, 2), # Күтілетін нәтиже: Қате: x [0..999] диапазонында болуы керек.
    (1000, 5), # Күтілетін нәтиже: Қате: x [0..999] диапазонында болуы керек.
    (100, 0), # Күтілетін нәтиже: Қате: n [1..100] диапазонында болуы керек.
    (100, 200), # Күтілетін нәтиже: Қате: n [1..100] диапазонында болуы керек.
    ('ADS', 'ASD'), # Күтілетін нәтиже: Қате: сандық аргумент енгізіңіз.
    (999, 1), # Күтілетін нәтиже: 999
    (0, 100) # Күтілетін нәтиже: 0
]

for x, n in test_cases:
    print(f"power({x}, {n}) = {power(x, n)}")

